defmodule ExAIS.Data.Decoders.WeatherReport do
  @moduledoc """
  Parses DAC 1, FI 31 (weather) AIS payload into raw numeric field values.
  """

  defstruct [
    :air_pressure_trend,
    :air_pressure,
    :air_temp,
    :current_direction_1,
    :current_direction_2,
    :current_direction_3,
    :current_measuring_level_2,
    :current_measuring_level_3,
    :current_speed_1,
    :current_speed_2,
    :current_speed_3,
    :dew_point,
    :horizontal_visibility,
    :humidity,
    :ice,
    :latitude,
    :longitude,
    :pos_accuracy,
    :precipitation_type,
    :salinity,
    :sea_state,
    :significant_wave_height,
    :swell_direction,
    :swell_height,
    :swell_period,
    :utc_day,
    :utc_hour,
    :utc_minute,
    :water_level_trend,
    :water_level,
    :water_temperature,
    :wave_direction,
    :wave_period,
    :wind_dir,
    :wind_gust_dir,
    :wind_gust,
    :wind_speed
  ]

  @doc """
  Decode the *application payload* (already past the 56-bit Type 8 header).
  Returns raw values extracted from the bit mask without conversion.
  """
  def from_binary(<<
        longitude::signed-25,
        latitude::signed-24,
        pos_accuracy::1,
        utc_day::5,
        utc_hour::5,
        utc_min::6,
        wind_speed::7,
        wind_gust::7,
        wind_dir::9,
        gust_dir::9,
        air_temp::signed-11,
        humidity::7,
        dew_point::signed-10,
        pressure::9,
        pressure_trend::2,
        horizontal_visibility::8,
        water_level::12,
        water_level_trend::2,
        current_speed_1::8,
        current_direction_1::9,
        current_speed_2::8,
        current_direction_2::9,
        current_measuring_level_2::5,
        current_speed_3::8,
        current_direction_3::9,
        current_measuring_level_3::5,
        significant_wave_height::8,
        wave_period::6,
        wave_direction::9,
        swell_height::8,
        swell_period::6,
        swell_direction::9,
        sea_state::4,
        water_temperature::signed-10,
        precipitation_type::3,
        salinity::9,
        ice::2,
        _rest::bitstring
      >>) do
    %__MODULE__{
      air_pressure_trend: pressure_trend,
      air_pressure: pressure,
      air_temp: air_temp,
      current_direction_1: current_direction_1,
      current_direction_2: current_direction_2,
      current_direction_3: current_direction_3,
      current_measuring_level_2: current_measuring_level_2,
      current_measuring_level_3: current_measuring_level_3,
      current_speed_1: current_speed_1,
      current_speed_2: current_speed_2,
      current_speed_3: current_speed_3,
      dew_point: dew_point,
      horizontal_visibility: horizontal_visibility,
      humidity: humidity,
      ice: ice,
      longitude: longitude / 600_000.0,
      latitude: latitude / 600_000.0,
      pos_accuracy: pos_accuracy,
      precipitation_type: precipitation_type,
      salinity: salinity,
      sea_state: sea_state,
      significant_wave_height: significant_wave_height,
      swell_direction: swell_direction,
      swell_height: swell_height,
      swell_period: swell_period,
      utc_day: utc_day,
      utc_hour: utc_hour,
      utc_minute: utc_min,
      water_level_trend: water_level_trend,
      water_level: water_level,
      water_temperature: water_temperature,
      wave_direction: wave_direction,
      wave_period: wave_period,
      wind_dir: wind_dir,
      wind_gust_dir: gust_dir,
      wind_gust: wind_gust,
      wind_speed: wind_speed
    }
  end
end
